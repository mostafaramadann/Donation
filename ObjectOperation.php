<?php


class ObjectOperation
{
private $objectamount;
private $objectworth;

    public function getObjectamount()
    {
        return $this->objectamount;
    }

    public function setObjectamount($objectamount)
    {
        $this->objectamount = $objectamount;
    }

    public function getObjectworth()
    {
        return $this->objectworth;
    }

    public function setObjectworth($objectworth)
    {
        $this->objectworth = $objectworth;
    }

}